// HINTS:
// Loop through first array to check the medicine
// then loop through second array to see if the symptoms match the inputted symptoms
// if they match add them into a new array at the end return array
// edge case if no matches return null
function webMD(ailments, meds) { 

    let map = new Map();
    for (let symptom of ailments) {
        for (let medicine of medications) {
            if (medicine.treatableSymptoms.includes(symptom)) {
                if (map.has(medicine.name)) {
                    map.set(medicine.name, map.get(medicine.name) +1);
                } else {
                    map.set(medicine.name, 1);
                }
            }
        }
    }

    let result = [];
    let max = 0;

    for(const [key, value] of map) {
        if (value > max) {
            max = value;
            result = []
            result.push(key);
        } else if (value == max) {
            result.push(key)
        }
    }

    return result;
}

